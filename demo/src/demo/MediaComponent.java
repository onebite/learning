package demo;

public class MediaComponent {
	private String af_application_id;
	private String media_type;
	private String flow_status;
	
	public String getMedia_type() {
		return media_type;
	}
	public void setMedia_type(String media_type) {
		this.media_type = media_type;
	}
	public String getAf_application_id() {
		return af_application_id;
	}
	public void setAf_application_id(String af_application_id) {
		this.af_application_id = af_application_id;
	}
	public String getFlow_status() {
		return flow_status;
	}
	public void setFlow_status(String flow_status) {
		this.flow_status = flow_status;
	}
}
